def get_role(work: str) -> str:
    """获取指定 work 的角色定义和核心规则"""
    config = ROLES.get(work)
    if not config:
        # 默认使用简历助手角色
        config = ROLES["resume"]
    return f"{config['role']}\n\n{config['rules']}"
